package com.capgemeini.junit;

public class Calculator {
	public int add(int a, int b) {
		return a + b;
	}// end of the add()

	public int sub(int x, int y) {
		return x - y;
	}// end of sub()

	public int mul(int w, int z) {
		return w * z;
	}// end of the mul()

	public int div(int c, int d) {
		return c / d;
	}// end of the div()

}//end of the class
